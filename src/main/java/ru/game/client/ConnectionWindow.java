import javax.swing.*;// –≤—Å–µ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç—ã UI –∏–∑ Swing
import java.awt.*; //–¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –≥—Ä–∞—Ñ–∏–∫–æ–π
