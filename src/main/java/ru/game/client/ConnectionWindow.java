        // –∑–∞–≥–æ–ª–æ–≤–æ–∫ –æ–∫–Ω–∞
        setTitle("üé® –†–∏—Å—É–π –∏ –£–≥–∞–¥—ã–≤–∞–π - –ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ");

        // –¥–µ–π—Å—Ç–≤–∏–µ –ø—Ä–∏ –∑–∞–∫—Ä—ã—Ç–∏–∏ –æ–∫–Ω–∞–ø—Ä–æ–≥—Ä–∞–º–º–∞ –ø–æ–ª–Ω–æ—Å—Ç—å—é –∑–∞–≤–µ—Ä—à–∏—Ç—Å—è
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        
        // —Ä–∞–∑–º–µ—Ä –æ–∫–Ω–∞ –≤ –ø–∏–∫—Å–µ–ª—è—Ö
        setSize(450, 250);
        
        // —Ü–µ–Ω—Ç—Ä–∏—Ä—É–µ–º –æ–∫–Ω–æ –Ω–∞ —ç–∫—Ä–∞–Ω–µ
        setLocationRelativeTo(null);
        
        // –∑–∞–ø—Ä–µ—â–∞–µ–º –∏–∑–º–µ–Ω—è—Ç—å —Ä–∞–∑–º–µ—Ä –æ–∫–Ω–∞
        setResizable(false);
        
        // –≥–ª–∞–≤–Ω—ã–π –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä –¥–ª—è –≤—Å–µ—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤
        // BorderLayout –º–µ–Ω–µ–¥–∂–µ—Ä –∫–æ–º–ø–æ–Ω–æ–≤–∫–∏ —Ä–∞–∑–º–µ—â–∞–µ—Ç —ç–ª–µ–º–µ–Ω—Ç—ã –ø–æ —Å–µ–≤–µ—Ä—É, —é–≥—É, –≤–æ—Å—Ç–æ–∫—É, –∑–∞–ø–∞–¥—É, —Ü–µ–Ω—Ç—Ä)
        // –æ—Ç—Å—Ç—É–ø—ã –º–µ–∂–¥—É —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏ –ø–æ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª–∏ –∏ –≤–µ—Ä—Ç–∏–∫–∞–ª–∏
        JPanel mainPanel = new JPanel(new BorderLayout(10, 10));
        
        // –≤–Ω—É—Ç—Ä–µ–Ω–Ω–∏–µ –æ—Ç—Å—Ç—É–ø—ã –æ–∫–Ω–∞
        mainPanel.setBorder(BorderFactory.createEmptyBorder(20, 20, 20, 20));
        
        // —Å–æ–∑–¥–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –æ–∫–Ω–∞
        // JLabel –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è —Ç–µ–∫—Å—Ç–∞
        // –≤—ã—Ä–æ–≤–Ω–µ–Ω –ø–æ —Ü–µ–Ω—Ç—Ä—É
        JLabel titleLabel = new JLabel("üé® –†–∏—Å—É–π –∏ –£–≥–∞–¥—ã–≤–∞–π üé®", JLabel.CENTER);

        //  —à—Ä–∏—Ñ—Ç Arial
        //  –∂–∏—Ä–Ω—ã–π
        //  20 –ø–∏–∫—Å–µ–ª–µ–π
        titleLabel.setFont(new Font("Arial", Font.BOLD, 20));
        
        // –¥–æ–±–∞–≤–ª—è–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫ –≤ –≤–µ—Ä—Ö–Ω—é—é —á–∞—Å—Ç—å –≥–ª–∞–≤–Ω–æ–π –ø–∞–Ω–µ–ª–∏
        mainPanel.add(titleLabel, BorderLayout.NORTH);
        
        // —Å–æ–∑–¥–∞–µ–º –ø–∞–Ω–µ–ª—å –¥–ª—è —Ñ–æ—Ä–º—ã
        JPanel formPanel = new JPanel(new GridBagLayout());
        
        // –æ–±—ä–µ–∫—Ç –¥–ª—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ —Ä–∞–∑–º–µ—â–µ–Ω–∏—è —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ GridBagLayout
        // GridBagConstraints –Ω–∞—Å—Ç—Ä–æ–π–∫–∏, –≥–¥–µ –∏ –∫–∞–∫ —Ä–∞–∑–º–µ—Å—Ç–∏—Ç—å —ç–ª–µ–º–µ–Ω—Ç
        GridBagConstraints gbc = new GridBagConstraints();
        
        // –æ—Ç—Å—Ç—É–ø—ã –≤–æ–∫—Ä—É–≥ —ç–ª–µ–º–µ–Ω—Ç–æ–≤
        gbc.insets = new Insets(5, 5, 5, 5);
        
        // –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–Ω–∏–µ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –ø–æ –ª–µ–≤–æ–º—É –∫—Ä–∞—é
        gbc.anchor = GridBagConstraints.WEST;
        
        // –¥–æ–±–∞–≤–ª—è–µ–º –ø–æ–ª–µ –¥–ª—è –≤–≤–æ–¥–∞ –Ω–∏–∫–Ω–µ–π–º–∞
        // —Ç–µ–∫—Å—Ç "–í–∞—à –Ω–∏–∫–Ω–µ–π–º:"
        gbc.gridx = 0;  // –ø–µ—Ä–≤—ã–π —Å—Ç–æ–ª–±–µ—Ü
        gbc.gridy = 0;  // –ø–µ—Ä–≤–∞—è —Å—Ç—Ä–æ–∫–∞
        formPanel.add(new JLabel("–í–∞—à –Ω–∏–∫–Ω–µ–π–º:"), gbc);
        
        // –¥–æ–±–∞–≤–ª—è–µ–º –ø–æ–ª–µ –≤–≤–æ–¥–∞ —Ä—è–¥–æ–º —Å –º–µ—Ç–∫–æ–π
        gbc.gridx = 1;  // —Å—Ç–æ–ª–±–µ—Ü 1
        // –ø–æ–ª–µ –±—É–¥–µ—Ç —Ä–∞—Å—Ç—è–≥–∏–≤–∞—Ç—å—Å—è –ø–æ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª–∏
        gbc.fill = GridBagConstraints.HORIZONTAL;
        // –ø–æ–ª–µ –∑–∞–π–º–µ—Ç –≤—Å–µ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–æ–µ –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–æ
        gbc.weightx = 1.0;
        
        // –ø–æ–ª–µ –¥–ª—è –≤–≤–æ–¥–∞ –Ω–∏–∫–Ω–µ–π–º–∞
        // 15 —à–∏—Ä–∏–Ω–∞ –ø–æ–ª—è –≤ —Å–∏–º–≤–æ–ª–∞—Ö
        // –≥–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Å–ª—É—á–∞–π–Ω—ã–π –Ω–∏–∫–Ω–µ–π–º "–ò–≥—Ä–æ–∫" + —Å–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ –æ—Ç 0 –¥–æ 999
        nicknameField = new JTextField("–ò–≥—Ä–æ–∫" + (int)(Math.random() * 1000), 15);
        
        // –ø–æ–ª–µ –≤ –ø–∞–Ω–µ–ª—å —Ñ–æ—Ä–º—ã
        formPanel.add(nicknameField, gbc);
        
        // –¥–ª—è –∞–¥—Ä–µ—Å–∞ —Å–µ—Ä–≤–µ—Ä–∞
        gbc.gridx = 0;
        gbc.gridy = 1;
        // —Å–±—Ä–∞—Å—ã–≤–∞–µ–º –Ω–∞—Å—Ç—Ä–æ–π–∫–∏
        gbc.fill = GridBagConstraints.NONE;  // –Ω–µ —Ä–∞—Å—Ç—è–≥–∏–≤–∞–µ–º –º–µ—Ç–∫—É
        gbc.weightx = 0;  // –º–µ—Ç–∫–∞ –Ω–µ –∑–∞–Ω–∏–º–∞–µ—Ç –ª–∏—à–Ω–µ–µ –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–æ
        formPanel.add(new JLabel("–ê–¥—Ä–µ—Å:"), gbc);
        
        // –ø–æ–ª–µ –≤–≤–æ–¥–∞ –∞–¥—Ä–µ—Å–∞
        gbc.gridx = 1;
        gbc.fill = GridBagConstraints.HORIZONTAL;  // —Ä–∞—Å—Ç—è–≥–∏–≤–∞–µ–º –ø–æ–ª–µ
        gbc.weightx = 1.0;  // –∑–∞–Ω–∏–º–∞–µ—Ç –≤—Å–µ –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–æ
        hostField = new JTextField("localhost", 15);
        formPanel.add(hostField, gbc);
        
        // –ø–æ–ª–µ –¥–ª—è –≤–≤–æ–¥–∞ –ø–æ—Ä—Ç–∞
        gbc.gridx = 0;
        gbc.gridy = 2;
        gbc.fill = GridBagConstraints.NONE;  // –Ω–µ —Ä–∞—Å—Ç—è–≥–∏–≤–∞–µ–º –º–µ—Ç–∫—É
        gbc.weightx = 0;  // –º–µ—Ç–∫–∞ –Ω–µ –∑–∞–Ω–∏–º–∞–µ—Ç –ª–∏—à–Ω–µ–µ –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–æ
        formPanel.add(new JLabel("–ü–æ—Ä—Ç:"), gbc);
        
        // –ø–æ–ª–µ –≤–≤–æ–¥–∞ –ø–æ—Ä—Ç–∞
        gbc.gridx = 1;
        gbc.fill = GridBagConstraints.HORIZONTAL;  // —Ä–∞—Å—Ç—è–≥–∏–≤–∞–µ–º –ø–æ–ª–µ
        gbc.weightx = 1.0;  // –∑–∞–Ω–∏–º–∞–µ—Ç –≤—Å–µ –ø—Ä–æ—Å—Ç—Ä–∞–Ω—Å—Ç–≤–æ
        portField = new JTextField("8888", 15);
        formPanel.add(portField, gbc);
        
        // –ø–∞–Ω–µ–ª—å —Ñ–æ—Ä–º—ã –≤ —Ü–µ–Ω—Ç—Ä –≥–ª–∞–≤–Ω–æ–π –ø–∞–Ω–µ–ª–∏
        // —ç–ª–µ–º–µ–Ω—Ç –±—É–¥–µ—Ç –≤ —Ü–µ–Ω—Ç—Ä–µ
        mainPanel.add(formPanel, BorderLayout.CENTER);
        
        // —Å–æ–∑–¥–∞–µ–º –∫–Ω–æ–ø–∫—É –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è
        connectButton = new JButton("–ü–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è");
        
        // –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–±—ã—Ç–∏—è –Ω–∞–∂–∞—Ç–∏—è –Ω–∞ –∫–Ω–æ–ø–∫—É
        connectButton.addActionListener(e -> {
            // –ø–æ–ª—É—á–∞–µ–º —Ç–µ–∫—Å—Ç –∏–∑ –ø–æ–ª–µ–π –≤–≤–æ–¥–∞
            // —É–±–∏—Ä–∞–µ–º –ø—Ä–æ–±–µ–ª—ã
            String nickname = nicknameField.getText().trim();
            String host = hostField.getText().trim();
            String portText = portField.getText().trim();

            if (nickname.isEmpty()) {
                // –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –¥–∏–∞–ª–æ–≥–æ–≤–æ–µ –æ–∫–Ω–æ —Å –æ—à–∏–±–∫–æ–π
                // - this —ç—Ç–æ –æ–∫–Ω–æ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è
                // - ERROR_MESSAGE —Ç–∏–ø —Å–æ–æ–±—â–µ–Ω–∏—è –∏–∫–æ–Ω–∫–∞ –æ—à–∏–±–∫–∏
                JOptionPane.showMessageDialog(this, "–í–≤–µ–¥–∏—Ç–µ –Ω–∏–∫–Ω–µ–π–º!", "–û—à–∏–±–∫–∞", JOptionPane.ERROR_MESSAGE);
                // –Ω–µ –ø—Ä–æ–¥–æ–ª–∂–∞–µ–º –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ
                return;
            }
            
            // —Ç–µ–∫—Å—Ç –ø–æ—Ä—Ç–∞ –≤ —á–∏—Å–ª–æ
            int port;
            try {
                // –µ—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ –Ω–µ —á–∏—Å–ª–æ–º –æ—à–∏–±–∫–∞
                port = Integer.parseInt(portText);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "–ù–µ–≤–µ—Ä–Ω—ã–π –ø–æ—Ä—Ç!", "–û—à–∏–±–∫–∞", JOptionPane.ERROR_MESSAGE);
                // –≤—ã—Ö–æ–¥–∏–º
                return;
            }
            
            // –æ—Ç–∫–ª—é—á–∞–µ–º –∫–Ω–æ–ø–∫—É —á—Ç–æ–±—ã –Ω–µ –º–æ–≥ –Ω–∞–∂–∞—Ç—å –¥–≤–∞–∂–¥—ã
            connectButton.setEnabled(false);
            // –º–µ–Ω—è–µ–º —Ç–µ–∫—Å—Ç –∫–Ω–æ–ø–∫–∏, –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —á—Ç–æ –∏–¥–µ—Ç –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ
            connectButton.setText("–ü–æ–¥–∫–ª—é—á–µ–Ω–∏–µ...");
            
            // –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–º –ø–æ—Ç–æ–∫–µ
            new Thread(() -> {
                try {
                    // —Å–æ–∑–¥–∞–µ–º –æ–±—ä–µ–∫—Ç –∫–ª–∏–µ–Ω—Ç–∞ —Å –Ω–∏–∫–Ω–µ–π–º–æ–º
                    GameClient client = new GameClient(nickname);
                    
                    // –ø—ã—Ç–∞–µ–º—Å—è –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ —Å–µ—Ä–≤–µ—Ä—É
                    client.connect(host, port);
                    
                    // –µ—Å–ª–∏ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ —É—Å–ø–µ—à–Ω–æ –æ–±–Ω–æ–≤–ª—è–µ–º UI
                    // SwingUtilities.invokeLater() –±–µ–∑–æ–ø–∞—Å–Ω–æ –æ–±–Ω–æ–≤–∏—Ç UI –∏–∑ –¥—Ä—É–≥–æ–≥–æ –ø–æ—Ç–æ–∫–∞ edt
                    SwingUtilities.invokeLater(() -> {
                        // —Å–∫—Ä—ã–≤–∞–µ–º –æ–∫–Ω–æ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è
                        setVisible(false);
                        
                        // –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é –æ–±—Ä–∞—Ç–Ω–æ–≥–æ –≤—ã–∑–æ–≤–∞ –æ—Ç–∫—Ä–æ–µ—Ç –∏–≥—Ä–æ–≤–æ–µ –æ–∫–Ω–æ
                        callback.onConnected(client);
                    });
                } catch (java.net.ConnectException ex) {
                    // —ç–Ω–µ —É–¥–∞–µ—Ç—Å—è –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ —Å–µ—Ä–≤–µ—Ä—É
                    ex.printStackTrace();
                    
                    // –æ–±–Ω–æ–≤–ª—è–µ–º UI –≤ –ø–æ—Ç–æ–∫–µ Swing
                    SwingUtilities.invokeLater(() -> {
                        // –ø–æ–¥—Ä–æ–±–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
                        JOptionPane.showMessageDialog(ConnectionWindow.this,
                            "–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ —Å–µ—Ä–≤–µ—Ä—É!\n\n" +
                            "–£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ:\n" +
                            "1. –°–µ—Ä–≤–µ—Ä –∑–∞–ø—É—â–µ–Ω (–≤–∏–¥–∏—Ç–µ '–°–µ—Ä–≤–µ—Ä –∑–∞–ø—É—â–µ–Ω –Ω–∞ –ø–æ—Ä—Ç—É 8888')\n" +
                            "2. –ê–¥—Ä–µ—Å: localhost, –ü–æ—Ä—Ç: 8888\n" +
                            "3. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –∫–æ–Ω—Å–æ–ª—å —Å–µ—Ä–≤–µ—Ä–∞\n\n" +
                            "–û—à–∏–±–∫–∞: " + ex.getMessage(),
                            "–û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è", JOptionPane.ERROR_MESSAGE);
                        
                        // —Å–Ω–æ–≤–∞ –≤–∫–ª—é—á–∞–µ–º –∫–Ω–æ–ø–∫—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –º–æ–∂–µ—Ç –ø–æ–ø—Ä–æ–±–æ–≤–∞—Ç—å –µ—â–µ —Ä–∞–∑
                        connectButton.setEnabled(true);
                        // –∏—Å—Ö–æ–¥–Ω—ã–π —Ç–µ–∫—Å—Ç –∫–Ω–æ–ø–∫–∏
                        connectButton.setText("–ü–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è");
                    });
                } catch (IOException ex) {
                    ex.printStackTrace();
                    
                    // –æ–±–Ω–æ–≤–ª—è–µ–º UI –≤ –ø–æ—Ç–æ–∫–µ Swing
                    SwingUtilities.invokeLater(() -> {
                        // –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ
                        JOptionPane.showMessageDialog(ConnectionWindow.this,
                            "–û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è: " + ex.getMessage() + "\n\n" +
                            "–ü—Ä–æ–≤–µ—Ä—å—Ç–µ –∫–æ–Ω—Å–æ–ª—å –¥–ª—è –¥–µ—Ç–∞–ª–µ–π",
                            "–û—à–∏–±–∫–∞", JOptionPane.ERROR_MESSAGE);
                        
                        // —Å–Ω–æ–≤–∞ –≤–∫–ª—é—á–∞–µ–º –∫–Ω–æ–ø–∫—É
                        connectButton.setEnabled(true);
                        connectButton.setText("–ü–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è");
                    });
                }
            }).start();  // –∑–∞–ø—É—Å–∫–∞–µ–º –ø–æ—Ç–æ–∫
        });
        
